
public class Op {

	public Op(double a, double b) {
	}
	public double addNumbers(double a, double b) {
		double answer = a + b;
		return answer;
	}
	public double subNumbers(double a, double b) {
		double answer = a - b;
		return answer;
	}
    public double multNumbers(double a, double b){
    	double answer = a * b;
    	return answer;
    }
    public double divNumbers(double a, double b) {
    	double answer = a / b;
    	return answer;
    }
}
